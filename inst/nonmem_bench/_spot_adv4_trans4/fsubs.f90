      SUBROUTINE PK(ICALL,IDEF,THETA,IREV,EVTREC,NVNT,INDXS,IRGG,GG,NETAS)      
      USE NMPRD4P
      USE SIZES,     ONLY: DPSIZE,ISIZE
      USE PRDIMS,    ONLY: GPRD,HPRD,GERD,HERD,GPKD
      USE NMPRD_REAL,ONLY: ETA,EPS                                            
      USE NMPRD_INT, ONLY: MSEC=>ISECDER,MFIRST=>IFRSTDER,COMACT,COMSAV,IFIRSTEM
      USE NMPRD_INT, ONLY: MDVRES,ETASXI,NPDE_MODE
      USE NMPRD_REAL, ONLY: DV_LOQ
      USE NMPRD_INT, ONLY: IQUIT
      USE PROCM_INT, ONLY: NEWIND=>PNEWIF                                       
      USE NMBAYES_REAL, ONLY: LDF                                             
      IMPLICIT REAL(KIND=DPSIZE) (A-Z)                                          
      REAL(KIND=DPSIZE) :: EVTREC                                               
      SAVE
      INTEGER(KIND=ISIZE) :: FIRSTEM
      INTEGER(KIND=ISIZE) :: ICALL,IDEF,IREV,NVNT,INDXS,IRGG,NETAS              
      DIMENSION :: IDEF(7,*),THETA(*),EVTREC(IREV,*),INDXS(*),GG(IRGG,GPKD+1,*) 
      FIRSTEM=IFIRSTEM
      IF (ICALL <= 1) THEN                                                      
      CALL ASSOCNMPRD4
      IDEF(  1,001)= -9
      IDEF(  1,002)= -1
      IDEF(  1,003)=  0
      IDEF(  1,004)=  0
      IDEF(  2,003)=  0
      IDEF(  2,004)=  0
      IDEF(  3,001)=  6
      IDEF(  3,002)=  7
      IDEF(  3,003)=  8
      CALL GETETA(ETA)                                                          
      IF (IQUIT == 1) RETURN                                                    
      RETURN                                                                    
      ENDIF                                                                     
      IF (NEWIND /= 2) THEN
      IF (ICALL == 4) THEN
      CALL SIMETA(ETA)
      ELSE
      CALL GETETA(ETA)
      ENDIF
      IF (IQUIT == 1) RETURN
      ENDIF
 !  level            0
      B00001=DEXP(ETA(001)) 
      CL=THETA(001)*B00001 
      B00002=DEXP(ETA(002)) 
      VC=THETA(002)*B00002 
      VP=THETA(003) 
      Q2=THETA(004) 
      B00003=DEXP(ETA(003)) 
      KA=THETA(005)*B00003 
      V2=VC 
      V3=VP 
      Q=Q2 
      S1=1.D0 
      S2=V2 
      S3=V3 
      IF (FIRSTEM == 1) THEN
!                      A00032 = DERIVATIVE OF CL W.R.T. ETA(001)
      A00032=THETA(001)*B00001 
!                      A00036 = DERIVATIVE OF VC W.R.T. ETA(002)
      A00036=THETA(002)*B00002 
!                      A00040 = DERIVATIVE OF KA W.R.T. ETA(003)
      A00040=THETA(005)*B00003 
!                      A00043 = DERIVATIVE OF V2 W.R.T. ETA(002)
      A00043=A00036 
!                      A00045 = DERIVATIVE OF S2 W.R.T. ETA(002)
      A00045=A00043 
      GG(001,1,1)=CL
      GG(001,002,1)=A00032
      GG(002,1,1)=V2
      GG(002,003,1)=A00043
      GG(003,1,1)=Q
      GG(004,1,1)=V3
      GG(005,1,1)=KA
      GG(005,004,1)=A00040
      GG(006,1,1)=S1
      GG(007,1,1)=S2
      GG(007,003,1)=A00045
      GG(008,1,1)=S3
      ELSE
      GG(001,1,1)=CL
      GG(002,1,1)=V2
      GG(003,1,1)=Q
      GG(004,1,1)=V3
      GG(005,1,1)=KA
      GG(006,1,1)=S1
      GG(007,1,1)=S2
      GG(008,1,1)=S3
      ENDIF
      IF (MSEC == 1) THEN
!                      A00034 = DERIVATIVE OF A00032 W.R.T. ETA(001)
      A00034=THETA(001)*B00001 
!                      A00038 = DERIVATIVE OF A00036 W.R.T. ETA(002)
      A00038=THETA(002)*B00002 
!                      A00042 = DERIVATIVE OF A00040 W.R.T. ETA(003)
      A00042=THETA(005)*B00003 
!                      A00044 = DERIVATIVE OF A00043 W.R.T. ETA(002)
      A00044=A00038 
!                      A00046 = DERIVATIVE OF A00045 W.R.T. ETA(002)
      A00046=A00044 
      GG(001,002,002)=A00034
      GG(002,003,003)=A00044
      GG(005,004,004)=A00042
      GG(007,003,003)=A00046
      ENDIF
      RETURN
      END
      SUBROUTINE ERROR (ICALL,IDEF,THETA,IREV,EVTREC,NVNT,INDXS,F,G,HH)       
      USE NMPRD4P
      USE SIZES,     ONLY: DPSIZE,ISIZE
      USE PRDIMS,    ONLY: GPRD,HPRD,GERD,HERD,GPKD
      USE NMPRD_REAL,ONLY: ETA,EPS                                            
      USE NMPRD_INT, ONLY: MSEC=>ISECDER,MFIRST=>IFRSTDER,IQUIT,IFIRSTEM
      USE NMPRD_INT, ONLY: MDVRES,ETASXI,NPDE_MODE
      USE NMPRD_REAL, ONLY: DV_LOQ
      USE NMPRD_INT, ONLY: NEWL2
      USE PROCM_INT, ONLY: NEWIND=>PNEWIF                                       
      IMPLICIT REAL(KIND=DPSIZE) (A-Z)                                        
      REAL(KIND=DPSIZE) :: EVTREC                                             
      SAVE
      INTEGER(KIND=ISIZE) :: ICALL,IDEF,IREV,NVNT,INDXS                       
      DIMENSION :: IDEF(*),THETA(*),EVTREC(IREV,*),INDXS(*)                   
      REAL(KIND=DPSIZE) :: G(GERD,*),HH(HERD,*)                               
      INTEGER(KIND=ISIZE) :: FIRSTEM
      FIRSTEM=IFIRSTEM
      IF (ICALL <= 1) THEN                                                    
      CALL ASSOCNMPRD4
      IDEF(2)=-1
      IDEF(3)=000
      RETURN
      ENDIF
      IF (ICALL == 4) THEN
      IF (NEWL2 == 1) THEN
      CALL SIMEPS(EPS)
      IF (IQUIT == 1) RETURN
      ENDIF
      ENDIF
      D00001=G(001,1)
      D00002=G(002,1)
      D00003=G(003,1)
 !  level            0
      B00001=1.D0+EPS(001) 
      Y=F*B00001+EPS(002) 
!                      C00032 = DERIVATIVE OF Y W.R.T. EPS(001)
      C00032=F 
!                      C00033 = DERIVATIVE OF Y W.R.T. EPS(002)
      C00033=1.D0 
      IF (FIRSTEM == 1) THEN !1
!                      D00040 = DERIVATIVE OF Y W.R.T. ETA(003)
      D00040=B00001*D00003 
!                      D00041 = DERIVATIVE OF Y W.R.T. ETA(002)
      D00041=B00001*D00002 
!                      D00042 = DERIVATIVE OF Y W.R.T. ETA(001)
      D00042=B00001*D00001 
!                      D00049 = DERIVATIVE OF C00032 W.R.T. ETA(003)
      D00049=D00003 
!                      D00050 = DERIVATIVE OF C00032 W.R.T. ETA(002)
      D00050=D00002 
!                      D00051 = DERIVATIVE OF C00032 W.R.T. ETA(001)
      D00051=D00001 
      G(001,1)=D00042
      G(002,1)=D00041
      G(003,1)=D00040
      ENDIF !1
      HH(001,1)=C00032
      HH(002,1)=C00033
      IF (FIRSTEM == 1) THEN !2
      HH(001,002)=D00051
      HH(001,003)=D00050
      HH(001,004)=D00049
      ENDIF !2
      IF (MSEC == 1) THEN
      D00004=G(001,002)
      D00005=G(002,002)
      D00006=G(002,003)
      D00007=G(003,002)
      D00008=G(003,003)
      D00009=G(003,004)
!                      D00043 = DERIVATIVE OF D00040 W.R.T. ETA(003)
      D00043=B00001*D00009 
!                      D00044 = DERIVATIVE OF D00041 W.R.T. ETA(003)
      D00044=B00001*D00008 
!                      D00045 = DERIVATIVE OF D00041 W.R.T. ETA(002)
      D00045=B00001*D00006 
!                      D00046 = DERIVATIVE OF D00042 W.R.T. ETA(003)
      D00046=B00001*D00007 
!                      D00047 = DERIVATIVE OF D00042 W.R.T. ETA(002)
      D00047=B00001*D00005 
!                      D00048 = DERIVATIVE OF D00042 W.R.T. ETA(001)
      D00048=B00001*D00004 
      G(001,002)=D00048
      G(002,002)=D00047
      G(002,003)=D00045
      G(003,002)=D00046
      G(003,003)=D00044
      G(003,004)=D00043
      ENDIF
      F=Y
      RETURN
      END
