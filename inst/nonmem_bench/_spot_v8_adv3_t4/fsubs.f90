      MODULE NMPRD4P
      USE SIZES, ONLY: DPSIZE
      USE NMPRD4,ONLY: VRBL
      IMPLICIT NONE
      SAVE
      REAL(KIND=DPSIZE), DIMENSION (:),POINTER ::COM
      REAL(KIND=DPSIZE), POINTER ::CL,VC,VP,Q2,S1,S2,V1,V2,Q,Y,A00032
      REAL(KIND=DPSIZE), POINTER ::A00034,A00036,A00038,A00039,A00040
      REAL(KIND=DPSIZE), POINTER ::A00041,A00042
      CONTAINS
      SUBROUTINE ASSOCNMPRD4
      COM=>VRBL
      CL=>COM(00001);VC=>COM(00002);VP=>COM(00003);Q2=>COM(00004)
      S1=>COM(00005);S2=>COM(00006);V1=>COM(00007);V2=>COM(00008)
      Q=>COM(00009);Y=>COM(00010);A00032=>COM(00011)
      A00034=>COM(00012);A00036=>COM(00013);A00038=>COM(00014)
      A00039=>COM(00015);A00040=>COM(00016);A00041=>COM(00017)
      A00042=>COM(00018)
      END SUBROUTINE ASSOCNMPRD4
      END MODULE NMPRD4P
      SUBROUTINE PK(ICALL,IDEF,THETA,IREV,EVTREC,NVNT,INDXS,IRGG,GG,NETAS)      
      USE NMPRD4P
      USE SIZES,     ONLY: DPSIZE,ISIZE
      USE PRDIMS,    ONLY: GPRD,HPRD,GERD,HERD,GPKD
      USE NMPRD_REAL,ONLY: ETA,EPS                                            
      USE NMPRD_INT, ONLY: MSEC=>ISECDER,MFIRST=>IFRSTDER,COMACT,COMSAV,IFIRSTEM
      USE NMPRD_INT, ONLY: MDVRES,ETASXI,NPDE_MODE
      USE NMPRD_REAL, ONLY: DV_LOQ
      USE NMPRD_INT, ONLY: IQUIT
      USE PROCM_INT, ONLY: NEWIND=>PNEWIF                                       
      USE NMBAYES_REAL, ONLY: LDF                                             
      IMPLICIT REAL(KIND=DPSIZE) (A-Z)                                          
      REAL(KIND=DPSIZE) :: EVTREC                                               
      SAVE
      INTEGER(KIND=ISIZE) :: FIRSTEM
      INTEGER(KIND=ISIZE) :: ICALL,IDEF,IREV,NVNT,INDXS,IRGG,NETAS              
      DIMENSION :: IDEF(7,*),THETA(*),EVTREC(IREV,*),INDXS(*),GG(IRGG,GPKD+1,*) 
      FIRSTEM=IFIRSTEM
      IF (ICALL <= 1) THEN                                                      
      CALL ASSOCNMPRD4
      IDEF(  1,001)= -9
      IDEF(  1,002)= -1
      IDEF(  1,003)=  0
      IDEF(  1,004)=  0
      IDEF(  2,003)=  0
      IDEF(  2,004)=  0
      IDEF(  3,001)=  5
      IDEF(  3,002)=  6
      CALL GETETA(ETA)                                                          
      IF (IQUIT == 1) RETURN                                                    
      RETURN                                                                    
      ENDIF                                                                     
      IF (NEWIND /= 2) THEN
      IF (ICALL == 4) THEN
      CALL SIMETA(ETA)
      ELSE
      CALL GETETA(ETA)
      ENDIF
      IF (IQUIT == 1) RETURN
      ENDIF
 !  level            0
      B00001=DEXP(ETA(001)) 
      CL=THETA(001)*B00001 
      B00002=DEXP(ETA(002)) 
      VC=THETA(002)*B00002 
      VP=THETA(003) 
      Q2=THETA(004) 
      S1=VC 
      S2=VP 
      V1=VC 
      V2=VP 
      Q=Q2 
      IF (FIRSTEM == 1) THEN
!                      A00032 = DERIVATIVE OF CL W.R.T. ETA(001)
      A00032=THETA(001)*B00001 
!                      A00036 = DERIVATIVE OF VC W.R.T. ETA(002)
      A00036=THETA(002)*B00002 
!                      A00039 = DERIVATIVE OF S1 W.R.T. ETA(002)
      A00039=A00036 
!                      A00041 = DERIVATIVE OF V1 W.R.T. ETA(002)
      A00041=A00036 
      GG(001,1,1)=CL
      GG(001,002,1)=A00032
      GG(002,1,1)=V1
      GG(002,003,1)=A00041
      GG(003,1,1)=Q
      GG(004,1,1)=V2
      GG(005,1,1)=S1
      GG(005,003,1)=A00039
      GG(006,1,1)=S2
      ELSE
      GG(001,1,1)=CL
      GG(002,1,1)=V1
      GG(003,1,1)=Q
      GG(004,1,1)=V2
      GG(005,1,1)=S1
      GG(006,1,1)=S2
      ENDIF
      IF (MSEC == 1) THEN
!                      A00034 = DERIVATIVE OF A00032 W.R.T. ETA(001)
      A00034=THETA(001)*B00001 
!                      A00038 = DERIVATIVE OF A00036 W.R.T. ETA(002)
      A00038=THETA(002)*B00002 
!                      A00040 = DERIVATIVE OF A00039 W.R.T. ETA(002)
      A00040=A00038 
!                      A00042 = DERIVATIVE OF A00041 W.R.T. ETA(002)
      A00042=A00038 
      GG(001,002,002)=A00034
      GG(002,003,003)=A00042
      GG(005,003,003)=A00040
      ENDIF
      RETURN
      END
      SUBROUTINE ERROR (ICALL,IDEF,THETA,IREV,EVTREC,NVNT,INDXS,F,G,HH)       
      USE NMPRD4P
      USE SIZES,     ONLY: DPSIZE,ISIZE
      USE PRDIMS,    ONLY: GPRD,HPRD,GERD,HERD,GPKD
      USE NMPRD_REAL,ONLY: ETA,EPS                                            
      USE NMPRD_INT, ONLY: MSEC=>ISECDER,MFIRST=>IFRSTDER,IQUIT,IFIRSTEM
      USE NMPRD_INT, ONLY: MDVRES,ETASXI,NPDE_MODE
      USE NMPRD_REAL, ONLY: DV_LOQ
      USE NMPRD_INT, ONLY: NEWL2
      USE PROCM_INT, ONLY: NEWIND=>PNEWIF                                       
      IMPLICIT REAL(KIND=DPSIZE) (A-Z)                                        
      REAL(KIND=DPSIZE) :: EVTREC                                             
      SAVE
      INTEGER(KIND=ISIZE) :: ICALL,IDEF,IREV,NVNT,INDXS                       
      DIMENSION :: IDEF(*),THETA(*),EVTREC(IREV,*),INDXS(*)                   
      REAL(KIND=DPSIZE) :: G(GERD,*),HH(HERD,*)                               
      INTEGER(KIND=ISIZE) :: FIRSTEM
      FIRSTEM=IFIRSTEM
      IF (ICALL <= 1) THEN                                                    
      CALL ASSOCNMPRD4
      IDEF(2)=2
      IDEF(1)=1
      HH(1,1)=1.0D0
      IDEF(3)=000
      RETURN
      ENDIF
      IF (ICALL == 4) THEN
      IF (NEWL2 == 1) THEN
      CALL SIMEPS(EPS)
      IF (IQUIT == 1) RETURN
      ENDIF
      ENDIF
 !  level            0
      B00001=1.D0+EPS(001) 
      Y=F*B00001 
      IF (FIRSTEM == 1) THEN !1
      ENDIF !1
      F=Y
      RETURN
      END
